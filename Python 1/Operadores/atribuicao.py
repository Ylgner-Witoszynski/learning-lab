#atribuição simples
saldo = 1000
print(saldo)

#atribuição com adição
saldo = 1000
"""saldo += 200"""
saldo = saldo + 200
print(saldo)

#atribuição com subtração
saldo = 1000 
"""saldo -= 200"""
saldo = saldo - 200
print(saldo)

#atribuição com multiplicação
saldo = 1000 
"""saldo *= 200"""
saldo = saldo * 2
print(saldo)

#atribuição com divisão
saldo = 1000 
"""saldo /= 200"""
saldo = saldo / 2
print(saldo)

#atribuição com módulo
saldo = 1000 
"""saldo %= 200"""
saldo = saldo % 2
print(saldo)

#atribuição com exponenciação
saldo = 1000 
"""saldo **= 200"""
saldo = saldo ** 2
print(saldo)

#atribuição com divisão inteira
saldo = 1000 
"""saldo //= 200"""
saldo = saldo // 2
print(saldo)
